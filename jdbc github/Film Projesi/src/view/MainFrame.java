    public void tabloyuDoldur(){
        filmler = filmDao.butunFilmleriGetir();
        
        String data[][] = new String[filmler.size()][];
        
        for(int i=0; i<filmler.size(); i++){
            data[i] = new String[]{String.valueOf(filmler.get(i).getFilmId()),
            filmler.get(i).getFilmAdi(),
            filmler.get(i).getFilmYili(),
            filmler.get(i).getKategori().getKategoriAdi(),
            filmler.get(i).getYonetmen().getYonetmenAdi()};
        }
          
        tblFilmler.setModel(new DefaultTableModel(data, 
                new String[] {"FILM_ID","FILM_ADI","FILM_YILI","KATEGORI_ADI","YONETMEN_ADI"}));
        
    }

